void solve(int n){
    int i,size=0,arr[n],count=0,max=0;
    // arr[] -> binary number in reverse order
    while(n!=0){
        i=n%2;
        n=n/2;
        arr[size++]=i;
    }
    for(i=0;i<size;i++){
        if(arr[i]==1)
            count++;
        // If 0 encountered, check if count is more than max and then reset to 0 for next set of 1's
        else{
            if(count>max)
                max=count;
            count=0;
        }
    }
    // If arr ends with 1, this if statement is required to verify which set has max 1's
    if(count>max)
        max=count;
    printf("%d",max);
}


